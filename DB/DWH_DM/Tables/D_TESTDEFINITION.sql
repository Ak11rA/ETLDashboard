	"TESTID" NUMBER NOT NULL ENABLE,
	"BESCHREIBUNG" VARCHAR2(255 CHAR) DEFAULT '~',
	"TYP" VARCHAR2(255 CHAR) DEFAULT '~',
	"SCHEMA" VARCHAR2(255 CHAR) DEFAULT '~',
	"TESTCODE_NR" VARCHAR2(4000 CHAR),
	"TESTCODE_TXT" VARCHAR2(4000 CHAR),
	 CONSTRAINT "D_DTSTDEF_PK" PRIMARY KEY ("DTSTDEF_ID")
  USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 COMPUTE STATISTICS
  TABLESPACE "DWH_DM"  ENABLE,
	 CONSTRAINT "D_TESTDEF_UK" UNIQUE ("TESTID")
  USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 COMPUTE STATISTICS
  TABLESPACE "DWH_DM"  ENABLE
   ) SEGMENT CREATION IMMEDIATE
  PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255
 NOCOMPRESS LOGGING
  TABLESPACE "DWH_DM"
  INMEMORY PRIORITY NONE MEMCOMPRESS FOR QUERY LOW
  DISTRIBUTE AUTO NO DUPLICATE ;

