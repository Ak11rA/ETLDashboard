/*-----------------------------------------------------------------------------
|| Tabellen-Skript der Tabelle C_DWH_JOBQUEUE
*/-----------------------------------------------------------------------------


  CREATE TABLE "DWH_DM"."C_DWH_JOBQUEUE"
   (	"JOB_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE,
	"COMMAND" VARCHAR2(50 CHAR) NOT NULL ENABLE,
	"SUBMITTED" DATE DEFAULT sysdate NOT NULL ENABLE,
	"EXECUTED" TIMESTAMP (6),
	"RESULT" VARCHAR2(50 CHAR),
	"OUTPUT" CLOB,
	"ERRORCODE" NUMBER,
	"ARG1" VARCHAR2(255 CHAR),
	 CONSTRAINT "C_DWH_JOBQUEUE_PK" PRIMARY KEY ("JOB_ID")
  USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 COMPUTE STATISTICS
  TABLESPACE "DWH_DM"  ENABLE
   ) SEGMENT CREATION IMMEDIATE
  PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255
 NOCOMPRESS LOGGING
  TABLESPACE "DWH_DM"
  INMEMORY PRIORITY NONE MEMCOMPRESS FOR QUERY LOW
  DISTRIBUTE AUTO NO DUPLICATE
 LOB ("OUTPUT") STORE AS SECUREFILE (
  TABLESPACE "DWH_DM" ENABLE STORAGE IN ROW CHUNK 8192
  NOCACHE LOGGING  NOCOMPRESS  KEEP_DUPLICATES ) ;

-------------------
-- Zugehörige Kommentare
-------------------

   COMMENT ON COLUMN "DWH_DM"."C_DWH_JOBQUEUE"."JOB_ID" IS 'ID/Nr des Jobs';
   COMMENT ON COLUMN "DWH_DM"."C_DWH_JOBQUEUE"."COMMAND" IS 'Auszuführender Befehl';
   COMMENT ON COLUMN "DWH_DM"."C_DWH_JOBQUEUE"."SUBMITTED" IS 'Datum, zu dem das Kommando angefordert wurde';
   COMMENT ON COLUMN "DWH_DM"."C_DWH_JOBQUEUE"."EXECUTED" IS 'Datum, zu dem das Kommando ausgeführt wurde';
   COMMENT ON COLUMN "DWH_DM"."C_DWH_JOBQUEUE"."RESULT" IS 'Ergebnis der Ausführung (Text)';
   COMMENT ON COLUMN "DWH_DM"."C_DWH_JOBQUEUE"."OUTPUT" IS 'Ausgabe des Kommandos';
   COMMENT ON COLUMN "DWH_DM"."C_DWH_JOBQUEUE"."ERRORCODE" IS 'Fehler-Nummer:

+1: erfolgreich
-1: allgemeiner Fehler
-2: Job läuft noch
-3: Job wurde mit Fehler beendet durch Ladeplan
<-1000: Job wurde von Exec Jobqueue beendet mit Anzahl Fehlern in ODI Log ';
   COMMENT ON COLUMN "DWH_DM"."C_DWH_JOBQUEUE"."ARG1" IS 'Argument 1 für die Ausführung, ODI_ID des Ladeplans';

